        <h2 class="presentation">
            "JoJo's Bizarre Adventure" (JoJo no Kimyou na Bouken) est une série de mangas japonais écrite et
            dessinée par Hirohiko Araki. <br>
            La série est connue pour son style unique, son intrigue complexe et ses combats excentriques. <br>
            "JoJo's Bizarre Adventure" suit la vie de la famille Joestar à travers les générations, chaque partie de
            la série se concentrant sur un descendant de la famille, surnommé JoJo.<br>
            Les JoJos sont souvent confrontés à des ennemis dotés de pouvoirs surnaturels, utilisant eux-mêmes des
            Stands (des manifestations physiques de la volonté et de l'énergie combative).<br>
            L'histoire mélange l'occulte, l'action, l'aventure et le surnaturel, créant une œuvre originale et
            influente dans le monde du manga et de l'anime.<br>
            La série a été adaptée en plusieurs saisons d'anime, gagnant une grande popularité au fil des années.
        </h2>

